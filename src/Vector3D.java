package src;

public class Vector3D {

    // QUESTION 1
    private double x;
    private double y;
    private double z;

    public Vector3D(double nX, double nY, double nZ) {
        this.x = nX;
        this.y = nY;
        this.z = nZ;
    }

    // QUESTION 2
    private double getX() {
        return x;
    }

    private double getY() {
        return y;
    }

    private double getZ() {
        return z;
    }

    // QUESTION 3


}
